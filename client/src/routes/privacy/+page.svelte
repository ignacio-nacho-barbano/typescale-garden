	<h1>Data Policy</h1>

	<p><em>Last Updated: <b>March 6 2024</b></em></p>

	<h2>1. Overview</h2>
	<p>
		This data policy outlines how Typescale Garden collects, uses, and protects user data when
		interacting with our services. We leverage Auth0 as a third-party authentication provider and
		utilize Hotjar and Google Analytics for analytics purposes.
	</p>
	<p />
	<h3>2. Information Collected</h3>
	<h4>We collect and store the following user information:</h4>
	<ul>
		<li>
			<strong>Basic Profile Information:</strong> Like Name and email as well as
			<a href="https://openid.net/specs/openid-connect-core-1_0.html#StandardClaims"
				>other public openId fields.</a
			>
		</li>
		<li>
			<strong>Authentication Data:</strong> Auth0 Tokens used to verify ownership of the different generated
			contents of Typescale Garden's users.
		</li>
		<li>
			<strong>Usage Analytics:</strong> Anonymous and encrypted usage data trough Hotjar and Google Analytics
			(GA4).
		</li>
	</ul>

	<h3>3. How We Use Your Data</h3>
	<h4>We use the collected data for the following purposes:</h4>
	<ul>
		<li><strong>User Authentication:</strong> Utilizing Auth0 for secure user authentication.</li>
		<li><strong>Personalization:</strong> Enhancing user experience based on preferences.</li>
		<li>
			<strong>Analytics:</strong> Improving our services through aggregated usage data with Hotjar and
			Google Analytics.
		</li>
	</ul>

	<h3>4. Data Security</h3>
	<h4>We prioritize the security of your data:</h4>
	<ul>
		<li>
			<strong>Third-Party Providers:</strong> Auth0, Hotjar, and Google Analytics are trusted for secure
			services.
		</li>
		<li>
			<strong>Encryption:</strong> Data transmission is encrypted using industry-standard protocols.
		</li>
	</ul>

	<h3>5. Data Retention</h3>
	<p>
		We retain user data for the duration necessary for the outlined purposes. Users can request data
